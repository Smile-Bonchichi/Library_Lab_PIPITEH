class Air {
    static double p;
    static int countAllHit;
    static int countAllShoot;
    static int countShoot;
    static int countHit;
private:
    static int check(int& n) {
        if ((n * p) >= 1) { airShoot(); return 1; }
        else { airMiss(); return 0; }
    }
public:
    static void shoot(int n) {
        countAllShoot++;
        countShoot += n;
        countHit += check(n);
        countAllHit += countHit;
    }
    static void result() {
        green("    : "); red(to_string(countAllShoot)); green(" \n");
        green("   : "); red(to_string(countAllHit)); green(" \n\n");
        green("    : "); red(to_string(countShoot)); green(" \n");
        green("   : "); red(to_string(countHit)); green(" \n");
        countShoot = countHit = 0;
    }
};

double Air::p = 0.25;
int Air::countAllHit = 0;
int Air::countAllShoot = 0;
int Air::countShoot = 0;
int Air::countHit = 0;

